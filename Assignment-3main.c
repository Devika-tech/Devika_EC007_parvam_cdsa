#include <stdio.h>
void main()
{
    int n;
    scanf("%d",&n);
    switch(n)
    {

        case1:
            printf("ONE");
            break;
        case2:
            printf("TWO");
            break;
        case3:
            printf("THREE");
            break;
        case4:
            printf("FOUR");
            break;
        case5:
            printf("FIVE");
            break;
        case6:
            printf("SIX");
            break;
        case7:
            printf("SEVEN");
            break;
        case8:
            printf("EIGHT");
            break;
        case9:
            printf("NINE");
            break;

    }
}
